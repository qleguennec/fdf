#include "fdf.h"

t_v3	mat_apply(t_v3 v3, double *mat)
{
	t_v3	v3_r;

	v3_r.x = v3.x * mat[0] + v3.y * mat[1] + v3.z * mat[2];
	v3_r.y = v3.x * mat[3] + v3.y * mat[4] + v3.z * mat[5];
	v3_r.z = v3.x * mat[6] + v3.y * mat[7] + v3.z * mat[8];
	return (v3_r);
}
